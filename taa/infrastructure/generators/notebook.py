"""Vertex AI / Jupyter notebook generator."""

from __future__ import annotations

import json


def _code_cell(source: str | list[str]) -> dict:
    """Create a Jupyter code cell."""
    if isinstance(source, str):
        source = source.split("\n")
    return {
        "cell_type": "code",
        "execution_count": None,
        "metadata": {},
        "outputs": [],
        "source": [line + "\n" for line in source],
    }


def _markdown_cell(source: str | list[str]) -> dict:
    """Create a Jupyter markdown cell."""
    if isinstance(source, str):
        source = source.split("\n")
    return {
        "cell_type": "markdown",
        "metadata": {},
        "source": [line + "\n" for line in source],
    }


